#include <iostream> // Includes the iostream library for input/output operations (درگیر کردن کتابخانه iostream برای عملیات ورودی/خروجی)

int main() { // The main function: program execution starts here (تابع اصلی: جایی که اجرای برنامه شروع می‌شود)

	std::cout << "Hello Project 1" << std::endl; // Prints text to the console (چاپ متن در خروجی کنسول)

	return 0; // Returns 0 to indicate successful execution (بازگرداندن 0 به معنای اجرای موفقیت‌آمیز برنامه)

}

