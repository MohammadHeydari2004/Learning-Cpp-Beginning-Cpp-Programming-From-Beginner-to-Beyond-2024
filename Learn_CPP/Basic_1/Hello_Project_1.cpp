#include <iostream> // Input/output stream library (کتابخانه جریان ورودی/خروجی)

int main() // (تابع اصلی که اجرای برنامه از آنجا شروع می‌شود). 
{
	std::cout << "Hello Project 1" << std::endl; // Print message (چاپ پیام)

	return 0;
}

/*
#include <iostream>  : Adds input/output capabilities
(افزودن قابلیت‌های ورودی/خروجی).

std::cout  : Outputs text to the console
(خروجی متن به کنسول).

<<  : Stream insertion operator
(عملگر قرار دادن در جریان).

"Hello Project 1"  : Text to display on console
(متنی که روی کنسول نمایش داده می‌شود).

std::endl  : Moves to a new line
(رفتن به خط جدید).

return 0  : Indicates successful program execution
(نشان‌دهنده اجرای موفقیت‌آمیز برنامه).
*/